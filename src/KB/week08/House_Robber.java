import java.util.HashMap;
import java.util.Map;

class House_Robber {
    static int[] nums;
    static Map<Integer, Integer> memo;

    public int rob(int[] nums) {
        this.nums = nums;
        memo = new HashMap<>();
        int n = nums.length;
        return dp(n);
    }

    public int dp(int n) {
        if (n == 1) return nums[0];
        if (n == 2) return Math.max(nums[0], nums[1]); // ì²« ë²ˆì§¸ ì§‘ ë˜ëŠ” ë‘ ë²ˆì§¸ ì§‘ ì¤‘ ëˆì´ ë” ë§ì€ ìª½ ì„ íƒ

        if (!memo.containsKey(n)) {
            // í˜„ì¬ ì§‘ì„ í„¸ì§€ ì•Šì„ ê²½ìš°: dp(n-1)
            // í˜„ì¬ ì§‘ì„ í„¸ ê²½ìš°: dp(n-2) + í˜„ì¬ ì§‘ì˜ ëˆ(nums[n-1])
            // ë‘ ê²½ìš° ì¤‘ ë” í° ê°’ì„ ì €ì¥
            memo.put(n, Math.max(dp(n - 1), dp(n - 2) + nums[n - 1]));
        }
        return memo.get(n);
    }
}